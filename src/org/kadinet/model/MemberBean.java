package org.kadinet.model;

public class MemberBean {
	private int member_no;
	private String member_img;
	private String member_name;
	private String member_position;
	private String member_etc;
	private String member_company;
	private String member_link;
	private int member_order;
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getMember_img() {
		return member_img;
	}
	public void setMember_img(String member_img) {
		this.member_img = member_img;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public String getMember_position() {
		return member_position;
	}
	public void setMember_position(String member_position) {
		this.member_position = member_position;
	}
	public String getMember_etc() {
		return member_etc;
	}
	public void setMember_etc(String member_etc) {
		this.member_etc = member_etc;
	}
	public String getMember_company() {
		return member_company;
	}
	public void setMember_company(String member_company) {
		this.member_company = member_company;
	}
	public String getMember_link() {
		return member_link;
	}
	public void setMember_link(String member_link) {
		this.member_link = member_link;
	}
	public int getMember_order() {
		return member_order;
	}
	public void setMember_order(int member_order) {
		this.member_order = member_order;
	}

}
