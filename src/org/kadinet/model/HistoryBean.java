package org.kadinet.model;

public class HistoryBean {
	private int history_no;
	private int history_year;
	private int history_month;
	private int history_day;
	private String history_data;
	
	public int getHistory_no() {
		return history_no;
	}
	public void setHistory_no(int history_no) {
		this.history_no = history_no;
	}
	public int getHistory_year() {
		return history_year;
	}
	public void setHistory_year(int history_year) {
		this.history_year = history_year;
	}
	public int getHistory_month() {
		return history_month;
	}
	public void setHistory_month(int history_month) {
		this.history_month = history_month;
	}
	public int getHistory_day() {
		return history_day;
	}
	public void setHistory_day(int history_day) {
		this.history_day = history_day;
	}
	public String getHistory_data() {
		return history_data;
	}
	public void setHistory_data(String history_data) {
		this.history_data = history_data;
	}
}
